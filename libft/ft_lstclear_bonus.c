#include "libft.h"

void	ft_lstclear(t_list **lst, void (*del)(void*))
{
	t_list	*tmp;

	if (!lst || !del)
		return ;
	while (*lst)
	{
		tmp = lst[0]->next;
		del(lst[0]->content);
		free (lst[0]);
		*lst = tmp;
	}
}
